def register():
    """Принимает на ввод строку и возвращает все слова с заглавными буквами"""
    s1 = input()
    s2 = s1.upper()
    return s2
    print(s2)

def up():
    """Принимает на ввод строку и возвращает каждое ее слово с заглавной буквы"""
    s1 = input()
    s2 = s1.title()
    return s2
    print(s2)
